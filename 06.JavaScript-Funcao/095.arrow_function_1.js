// Função anônima com function notation
let dobro = function (a) {
	return 2 * a;
};

// Função anônima com arrow function notation
dobro = (a) => {
	return 2 * a;
};

// Função anônima com arrow function notation (com retorno implícito)
dobro = (a) => 2 * a;

// Função anônima com function notation (sem parâmetros)
let hello = function () {
	return 'Olá';
};

// Função anônima com arrow function notation (sem parâmetros e retorno implícito)
hello = () => 'Olá';
